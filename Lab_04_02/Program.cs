 ├─────────────────┼────────────────────────┤
 │ -               │ аналитическая функция: │
 │                 │ analyticFunc = 0,5     │
 │                 │ расчетная функция:     │
 │                 │ calculationFunc = 0,5  │
 └─────────────────┴────────────────────────┘
