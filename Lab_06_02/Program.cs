 ├──────────────────────────────────────────────────────────────────────────────────────────────────────────┤
 │ Напишите программу, которая запрашивает у пользователя последовательность целых чисел, оканчивающуюся 5, │ 
 │ и определяет номер первого отрицательного элемента.                                                      │
 └──────────────────────────────────────────────────────────────────────────────────────────────────────────┘
 
 ┌─────────────────────────────────────────────────────────────────┐
 │ Выполнение программы.                                           │
