 │ Входные данные:           │ Выходные данные:                    │
 ├───────────────────────────┼─────────────────────────────────────┤
 │ последовательность чисел: │ номер первого отрицательного числа: │
 │ 1 8 3 -9 3 -1 5           │ position = 4                        │
